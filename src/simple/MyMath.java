public class MyMath {
	/**
	 * 進行a、b相加，並回傳結果
	 * @param a
	 * @param b
	 * @return
	 */
	public int add(int a, int b){
		return a + b;
	}
	
	/**
	 * 進行a、b相減，並回傳結果
	 * @param a
	 * @param b
	 * @return
	 */
	public int decrement(int a, int b){
		return a - b;
	}
}
